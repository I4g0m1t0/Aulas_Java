public class Professor {
    Integer IdProfessor;
    String Nome;
    String Departamento;
    
    public Professor (
        Integer IdProfessor, 
        String Nome, 
        String Departamento
    ) {
        this.IdProfessor = IdProfessor;
        this.Nome = Nome;
        this.Departamento = Departamento;
    }
}
