public class Tutor {
    Integer IdTutor;
    String Nome;
    String Telefone;
    String Email;

    public Tutor (
        Integer IdTutor,
        String Nome,
        String Telefone,
        String Email
    ) {
        this.IdTutor = IdTutor;
        this.Nome = Nome;
        this.Telefone = Telefone;
        this.Email = Email;
    }
}
